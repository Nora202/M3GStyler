import torch
import numpy
import math

def mse(img1, img2):
    return (((img1 - img2)) ** 2).view(img1.shape[0], -1).mean(1, keepdim=True)

def psnr(img1, img2):
    mse = (((img1 - img2)) ** 2).view(img1.shape[0], -1).mean(1, keepdim=True)
    if mse == 0:
        return 100
    return 20 * math.log10(1.0 / math.sqrt(mse))

def rmse(img1, img2):
    return torch.sqrt(torch.mean((img1 - img2) ** 2)).item()